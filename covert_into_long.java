public class covert_into_long {
    public static void main(String[] args) {
        double d = 25.222;
        long l = (long) d;
        int z = (int) l;
        System.out.println("double show : " + d);
        System.out.println("long show :" + l);
        System.out.println("integer show : " + z);


    }
}